public class Kysimus {
    private String riik;
    private String pealinn;

    public Kysimus(String riik, String pealinn) {
        this.riik = riik;
        this.pealinn = pealinn;
    }

    public String getRiik() {
        return riik;
    }

    public String getPealinn() {
        return pealinn;
    }
}
